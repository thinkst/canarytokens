<script setup lang="ts">
function handleStartAnimation() {
  const circle = document.querySelector('.logo-dot-front');

  // add <animate> elements to the circle
  const animateR = document.createElementNS(
    'http://www.w3.org/2000/svg',
    'animate'
  );

  // animate R element
  const attributesSize = {
    attributeName: 'r',
    from: '0',
    to: '400',
    dur: '1000ms',
    begin: '500ms',
    repeatCount: 'indefinite',
  };

  Object.entries(attributesSize).forEach(([key, value]) => {
    animateR.setAttribute(key, value);
  });

  circle?.appendChild(animateR);

  const animateOpacity = document.createElementNS(
    'http://www.w3.org/2000/svg',
    'animate'
  );

  // animate opacity circle
  const attributesOpacity = {
    attributeName: 'opacity',
    from: '1',
    to: '0',
    dur: '1000ms',
    begin: '500ms',
    repeatCount: 'indefinite',
  };
  Object.entries(attributesOpacity).forEach(([key, value]) => {
    animateOpacity.setAttribute(key, value);
  });

  circle?.appendChild(animateOpacity);
}

function handleStopAnimation() {
  const circle = document.querySelector('.logo-dot-front');
  // remove each animation element in the circle
  circle && Array.from(circle.childNodes).forEach((child) => child.remove());
}
</script>
